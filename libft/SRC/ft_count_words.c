#include "libft.h"

size_t	ft_count_words(const char *s, char c)
{
	size_t		i;
	size_t		n;

	n = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			i++;
		else
		{
			i++;
			if (s[i] == c || s[i] == '\0')
				n++;
		}
	}
	return (n);
}
