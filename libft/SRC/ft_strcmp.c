#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;
	int		res;

	i = 0;
	res = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
		i++;
	if (s1[i] != s2[i])
		res = ((unsigned char*)s1)[i] - ((unsigned char*)s2)[i];
	return (res);
}
